function bagi(a, b) {
  // eslint-disable-next-line no-undef
  hasilBagi = a / b;
  // eslint-disable-next-line no-undef
  return hasilBagi;
}

// memanggil fungsi
var nilai1 = ["33%", "98.1%", "56.44%", "100%"];
var nilai2 = [100, 100, 100, 100];
// eslint-disable-next-line no-unused-vars
var hasilPembagian = bagi(nilai1, nilai2);

